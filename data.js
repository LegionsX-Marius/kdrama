function getSeriesByCategory(category) {
    const result = [];
    for (const [id, series] of Object.entries(seriesData)) {
        if (series.category === category) {
            result.push({ id, ...series });
        }
    }
    return result;
}

function getSeriesById(id) {
    return seriesData[id] || null;
}

function getAllSeries() {
    return Object.entries(seriesData).map(([id, series]) => ({
        id,
        ...series
    }));
}

